#python ke data types aur variables
#sabse pehle hum data types ke baare mein baat karte hain. Python mein kuch common data types hain:
#1. int (integer): Ye data type pure numbers ko represent karta hai, jaise 1, 2, 3, -5, etc.
#2. float (floating point): Ye data type decimal numbers ko represent karta hai, jaise 3.14, -0.5, etc.
#3. str (string): Ye data type text ko represent karta hai, jaise "Hello", 'Python', etc.
#4. bool (boolean): Ye data type true ya false values ko represent karta hai, jaise True, False.
#5. list: Ye data type ordered collection of items ko represent karta hai, jaise [1, 2, 3], ['a', 'b', 'c'], etc.
#6. tuple: Ye data type ordered and immutable collection of items ko represent karta hai, jaise (1, 2, 3), ('a', 'b', 'c'), etc.
#7. dict (dictionary): Ye data type key-value pairs ko represent karta hai, jaise {'name': 'Alice', 'age': 30}, etc.
#8. set: Ye data type unordered collection of unique items ko represent karta hai, jaise {1, 2, 3}, {'a', 'b', 'c'}, etc.   

#example of data types
x = 10          # int
y = 3.14        # float
name = "Alice"  # str
