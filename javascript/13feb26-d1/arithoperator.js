let a = 5;
let b = 2;

console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a * b =", a * b);
console.log("a / b =", a / b); // / it gives the quotient (result) => 10 / 2 = 10
console.log("a % b =", a % b); //(modulus = %) it give the ramainder after division => 5 % 2 = a
console.log("a ** b =", a ** b); // (Exponentiation = **) it means a = 5 raised to the power b = 2 => a^b (5^2)